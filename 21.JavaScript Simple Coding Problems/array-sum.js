let array = [21, 891, 334, 324, 325432, 43432, 32432534,]
function arraySum(array) {
    let elementSum = 0;
    for (i = 0; i < array.length; i++) {
        let arrayElement = array[i];
        elementSum = elementSum = arrayElement;
    }
    return elementSum;
}
arraySumResult = arraySum(array);
console.log(arraySumResult)